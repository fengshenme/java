#include <stdio.h>
/*
C 作用域规则
任何一种编程中，作用域是程序中定义的变量所存在的区域，超过该区域变量就不能被访问。
C 语言中有三个地方可以声明变量：
在函数或块内部的局部变量
在所有函数外部的全局变量
在形式参数的函数参数定义中
让我们来看看什么是局部变量、全局变量和形式参数。
----
局部变量
在某个函数或块的内部声明的变量称为局部变量。它们只能被该函数或该代码块内部的语句使用。
局部变量在函数外部是不可知的。下面是使用局部变量的实例。在这里，所有的变量 a、b 和 c 是 main() 函数的局部变量。
*/ 
int main () {
  /* 局部变量声明 */
  int a, b;
  int c;
 
  /* 实际初始化 */
  a = 10;
  b = 20;
  c = a + b;
  printf ("value of a = %d, b = %d and c = %d\n", a, b, c);
  return 0;

}
